package umich.utils;

public class Pairs {

    private double[] array1;
    private double[] array2;
    public Pairs(double[] array1, double[] array2)
    {
        this.array1 = array1;
        this.array2 = array2;
    }
    public double[] getArray1() { return array1; }
    public double[] getArray2() { return array2; }
}
